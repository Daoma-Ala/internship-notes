/*
 * == Algo es igual a algo 
 * === Algo es igual a algo en valor y tipo
 * != Diferente de otro valor
 * !== Diferemte de otro valor y tipo de dato
 * > Mayor que otro valor
 * < Menor que otro valor
 * >= Mayor o igual
 * <= Menor o igual
 */

// Ejemplos
const a = 10;
const b = 20;
const c = "10";

console.log(a == b); // false
console.log(a === c); // false
console.log(a != b); // true
console.log(a !== c); // true
console.log(a > b); // false
console.log( a <= b); // true
console.log( a > c ); // false
